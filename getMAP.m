function [modes errors] = getMAP(feature1,feature2,feature3,feature4);
mode1_1 = sum(feature1(1,:))/length(feature1);
mode1_2 = sum(feature1(2,:))/length(feature1);
mode1_3 = sum(feature1(3,:))/length(feature1);
mode1 = [mode1_1;mode1_2;mode1_3];
mode2_1 = sum(feature2(1,:))/length(feature2);
mode2_2 = sum(feature2(2,:))/length(feature2);
mode2_3 = sum(feature2(3,:))/length(feature2);
mode2 = [mode2_1;mode2_2;mode2_3];
mode3_1 = sum(feature3(1,:))/length(feature3);
mode3_2 = sum(feature3(2,:))/length(feature3);
mode3_3 = sum(feature3(3,:))/length(feature3);
mode3 = [mode3_1;mode3_2;mode3_3];
mode4_1 = sum(feature4(1,:))/length(feature4);
mode4_2 = sum(feature4(2,:))/length(feature4);
mode4_3 = sum(feature4(3,:))/length(feature4);
mode4 = [mode4_1;mode4_2;mode4_3];
modes = [mode1 mode2 mode3 mode4];
error1_1 = (1-mode1_1)/mode1_1;
error1_2 = (4-mode1_2)/mode1_2;
error1_3 = (2-mode1_3)/mode1_3;
error1 = [error1_1;error1_2;error1_3];
error2_1 = (4-mode2_1)/mode2_1;
error2_2 = (3-mode2_2)/mode2_2;
error2_3 = (1-mode2_3)/mode2_3;
error2 = [error2_1;error2_2;error2_3];
error3_1 = (1-mode3_1)/mode3_1;
error3_2 = (2-mode3_2)/mode3_2;
error3_3 = (5-mode3_3)/mode3_3;
error3 = [error3_1;error3_2;error3_3];
error4_1 = (4-mode4_1)/mode4_1;
error4_2 = (1-mode4_2)/mode4_2;
error4_3 = (5-mode4_3)/mode4_3;
error4 = [error4_1;error4_2;error4_3];
errors = [error1 error2 error3 error4];